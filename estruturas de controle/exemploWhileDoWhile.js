// Exemplo WHILE

let valor01 = 12;
let valor02 = 10;

while (valor01 <= valor02) {
    console.log(`Valor atual: ${valor01}`);
    valor01++
}


console.log('=========================');


// Exemplo DO/WHILE

let valor03 = 12;
let valor04 = 10;

do {
    console.log(`Valor atual: ${valor03}`) // DO/WHILE garante pelo menos uma repetição.
} while (valor03 <= valor04);